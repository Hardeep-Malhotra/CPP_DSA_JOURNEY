#include <iostream>  // Header file for input-output
using namespace std; // Standard namespace

int main()
{

    int a = 10, b = 3; // Declare and initialize variables

    cout << a + b << endl; // Addition → 13
    cout << a - b << endl; // Subtraction → 7
    cout << a * b << endl; // Multiplication → 30
    cout << a / b << endl; // Division (integer division) → 3
    cout << a % b << endl; // Modulus (remainder) → 1

    return 0; // Program ends successfully
}
